package operator;

/**
 * @author Elke
 */
public class Operator01 {

    public static void main(String[] args){

        int a = 6;
        int b = 4;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);

        //整除是商
        System.out.println(a / b);
        //取余是余数
        System.out.println(a % b);

    }
}
