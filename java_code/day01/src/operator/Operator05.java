package operator;

/**
 * @author Elke
 */
public class Operator05 {
    public static void main(String[] args){

        int i = 10;
        double d = 30;
        int j = 30;

        System.out.println(i == d);
        System.out.println(i > d);
        System.out.println(i == j);
        System.out.println(d == j);
        System.out.println(d < j);
        System.out.println(i != j);
        System.out.println(d >= j);
    }
}
